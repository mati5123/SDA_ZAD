# PROSTA DEFINICJA CZYM JEST PROGRAMOWANIE?
"""
Programowanie to sposób pisania instrukcji dla komputera. Komputer nie może sam myśleć, więc musi otrzymać szczegółowe
instrukcje, które mu powiedzą, co ma zrobić. Te instrukcje nazywamy kodem programu, a pisząc kod, możemy tworzyć różne
programy, takie jak gry, aplikacje, strony internetowe i wiele innych.

Podobnie jak w przypadku pisania zwykłych zdań, kod programu musi być napisany w języku, który komputer może zrozumieć.
"""

# JAK DZIAŁA INTERPRETER?
"""
Python jest interpretowany, co oznacza, że kiedy wpisujesz kod źródłowy w pliku, komputer nie przetwarza go od razu na 
program. Zamiast tego, musisz użyć programu, który nazywa się "interpreter", żeby przeczytał i zrozumiał kod. 

Gdy interpreter czyta Twój kod, tłumaczy go na język, który komputer może zrozumieć, a następnie wykonuje go. 
Interpreter pracuje bardzo szybko, więc zwykle nie zauważysz, że tłumaczy kod.

Jedną z zalet Pythona jako języka interpretowanego jest to, że możesz łatwo testować swój kod i widzieć, co robi, 
ponieważ nie musisz go kompilować. To oznacza, że możesz zobaczyć wyniki swojego kodu natychmiast, a jeśli zrobiłeś 
coś źle, możesz szybko to poprawić.
"""

# CO OZNACZA, ŻE PYTHON JEST DYNAMICZNY TYPOWANY?
"""
Oznacza to, że nie musisz z góry określać typu danych, z którymi chcesz pracować. Typ danych to coś, co mówi 
komputerowi, jakie rodzaje informacji chcesz przechowywać i jak z nimi pracować.

Na przykład, gdy chcesz zapisać liczbę w swoim programie, możesz użyć liczby całkowitej (np. 7) lub liczby 
zmiennoprzecinkowej (np. 3.14). W innych językach programowania, takich jak Java lub C++, musisz określić, 
który typ liczby chcesz użyć, zanim zaczniesz pisać kod. W Pythonie nie musisz tego robić.
"""

